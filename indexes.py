course= 'python for beginners'
print(course[0]) #prints the first letter in the string
print(course[-1]) #prints the last letter in the string (-1)"
print(course[0:3]) # return letters from 0 to 3 not including 3
print(course[0:-1])
print(course[0:]) # returns all the letters from start to end
print(course[1:]) #will start to print from letter 1 to end
print(course[:5])# python will assume the start is 0and do it as 0:5
another = course[:]
print(another)

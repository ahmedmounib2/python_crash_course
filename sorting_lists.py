cars= ['bmw','tesla','maserati','jeep','ford','hyundai','seat','mercedes']
cars.sort() #sort a list alphabitcally
print(cars)
cars.sort(reverse=True) #sort a list opposite to alphabitic order
print(cars)

print("\n here is the original list:")
print(cars)

print("\n here is the sorted list:")
print(sorted(cars))

print("\n here is the original list again:")
print(cars)

print("\n here is the list with reverse")
cars.reverse() # this reverse the order of the list but not alphabitcally
print(cars)


places_to_visit= ['italy','america','london','france','denmark','espania']
print("\n, this is the original list:")
print(places_to_visit)
print("\n, this is the sorted places to visit:")
print(sorted(places_to_visit))

print("\n this is original list again")
print(places_to_visit)

print("\n, this is the list reversed")
places_to_visit.reverse()
print(places_to_visit)
places_to_visit.reverse()
print(places_to_visit)

print("\n, here is the list with sort")
places_to_visit.sort()
print(places_to_visit)

print("\n, here is the list in reverse alphabitical order")
places_to_visit.sort(reverse=True)
print(places_to_visit)

